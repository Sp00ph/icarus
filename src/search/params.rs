pub fn see_val(piece: Piece) -> i16 {
    match piece {
        Piece::Pawn => pawn_see_val(),
        Piece::Knight => knight_see_val(),
        Piece::Bishop => bishop_see_val(),
        Piece::Rook => rook_see_val(),
        Piece::Queen => queen_see_val(),
        Piece::King => 0,
    }
}

